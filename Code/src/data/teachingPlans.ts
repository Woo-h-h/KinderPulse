/** 获取所有领域标签 */
export function getAllDomains(): string[] {
  const domains = new Set<string>()
  presetTeachingPlans.forEach((plan) => {
    plan.domain.split('、').forEach((d) => {
      domains.add(d.replace(/[（）()]/g, '').trim())
    })
  })
  return ['全部', ...Array.from(domains)]
}
